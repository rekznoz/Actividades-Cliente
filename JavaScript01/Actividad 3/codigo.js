
let num1 = 10
let num2 = 5

let suma = num1 + num2             // Suma
let resta = num1 - num2            // Resta
let multiplicacion = num1 * num2   // Multiplicación
let division = num1 / num2         // División

console.log("Suma", suma) // 15
console.log("Resta", resta) // 5
console.log("Multiplicacion", multiplicacion) // 50
console.log("Division", division) // 2

num1++
console.log("Incremento", num1) // 11

num2--
console.log("Decremento", num2) // 4